<template>
  <section>
    <div class="ui middle aligned center aligned grid">
      <div class="column">
        <h1>Admin</h1>
        <p v-if="user">User: {{ user.email }}</p>
        <button class="btn btn-primary mb-4" @click="signout">Signout</button>
        <table class="table table-hover">
          <thead>
            <tr>
              <th scope="col">Email</th>
              <th scope="col">Role</th>
            </tr>
          </thead>
          <tbody>
            <tr v-for="item in users" :key="item.id">
              <td data-label="Name">{{ item.email }}</td>
              <td>
                <select @change="changeRole(user.id, $event)">
                  <option :selected="user.role.subscriber" value="subscriber">Subscriber</option>
                  <option :selected="user.role.customer" value="customer">Customer</option>
                </select>
              </td>
            </tr>
          </tbody>
        </table>
      </div>
    </div>
  </section>
</template>
<script>
export default {
  layout: 'aut',
  data() {
    return {
      users: [],
      user: null,
    }
  },
  created() {
    const self = this
    this.$firebase.auth().onAuthStateChanged((user) => {
      self.user = user
    })
    this.users = []
    this.$firebase
      .firestore()
      .collection('roles')
      .get()
      .then((snap) => {
        snap.forEach((doc) => {
          const user = doc.data()
          user.id = doc.id
          // eslint-disable-next-line no-console
          console.log('ðŸŒ¿', doc.data())
          if (!user.role.admin) this.users.push(user)
        })
      })
  },
  methods: {
    signout() {
      this.$firebase
        .auth()
        .signOut()
        .then((user) => {
          this.$router.push('/login')
        })
    },
    changeRole(uid, event) {
      const setUserRole = this.$firebase.functions().httpsCallable('setUserRole')
      const data = {
        uid,
        role: {
          [event.target.value]: true,
        },
      }
      setUserRole(data)
        .then((result) => {
          // eslint-disable-next-line no-console
          console.log('ðŸŽ‰', result)
        })
        .catch((error) => {
          // eslint-disable-next-line no-console
          console.log('ðŸ¤¡', error)
        })
    },
  },
}
</script>
